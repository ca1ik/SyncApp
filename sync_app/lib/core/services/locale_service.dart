/// Supported app locales. Order matches the picker order shown in Settings.
class AppLocale {
  const AppLocale(this.code, this.name, this.flag);
  final String code;
  final String name; // Native name (autonym)
  final String flag; // Emoji flag

  static const List<AppLocale> supported = [
    AppLocale('en', 'English', '🇬🇧'),
    AppLocale('tr', 'Türkçe', '🇹🇷'),
    AppLocale('zh', '中文', '🇨🇳'),
    AppLocale('es', 'Español', '🇪🇸'),
    AppLocale('hi', 'हिन्दी', '🇮🇳'),
    AppLocale('ar', 'العربية', '🇸🇦'),
    AppLocale('fr', 'Français', '🇫🇷'),
    AppLocale('pt', 'Português', '🇵🇹'),
    AppLocale('ru', 'Русский', '🇷🇺'),
    AppLocale('ja', '日本語', '🇯🇵'),
    AppLocale('de', 'Deutsch', '🇩🇪'),
    AppLocale('ko', '한국어', '🇰🇷'),
  ];

  static bool isSupported(String code) => supported.any((l) => l.code == code);
}

/// App-wide locale service supporting 12 languages.
/// Backward compatible: existing `l.tr(en, turk)` calls still work.
/// For non-EN/TR locales, the English source string is looked up in
/// [_translations] and falls back to English when not found.
class LocaleService extends ChangeNotifier {
  LocaleService._();
  static final LocaleService instance = LocaleService._();

  static const String _prefKey = 'app_locale';
  String _locale = 'en';

  String get locale => _locale;
  bool get isEnglish => _locale == 'en';
  bool get isTurkish => _locale == 'tr';
  bool get isRtl => _locale == 'ar';
  Locale get materialLocale => Locale(_locale);

  AppLocale get current => AppLocale.supported.firstWhere(
        (l) => l.code == _locale,
        orElse: () => AppLocale.supported.first,
      );

  Future<void> load() async {
    final prefs = await SharedPreferences.getInstance();
    final saved = prefs.getString(_prefKey);
    if (saved != null && AppLocale.isSupported(saved)) {
      _locale = saved;
    }
    notifyListeners();
  }

  Future<void> setLocale(String locale) async {
    if (!AppLocale.isSupported(locale)) return;
    if (_locale == locale) return;
    _locale = locale;
    notifyListeners();
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_prefKey, locale);
  }

  Future<void> toggle() async {
    await setLocale(_locale == 'en' ? 'tr' : 'en');
  }

  /// Translate. Backward compatible signature.
  /// - 'en' → returns [en]
  /// - 'tr' → returns [turk]
  /// - other → looks up [en] in [_translations]; falls back to [en].
  String tr(String en, String turk) {
    if (_locale == 'en') return en;
    if (_locale == 'tr') return turk;
    return _translations[en]?[_locale] ?? en;
  }
}

